"""AE-3: Analysis Depth eval.

Evaluates whether the report's analysis depth is appropriate for the query's
purpose. Uses an LLM judge to:
  1. Classify the query purpose (survey/comparison/deep-dive/exploratory)
  2. Apply purpose-specific depth criteria
  3. Score depth on a 1-5 scale

Purpose-specific criteria:
  - Survey: breadth of topics, papers mentioned, conciseness
  - Comparison: cross-paper analysis, dimensions compared, synthesis
  - Deep-dive: depth per paper, methodology details, limitations discussion
  - Exploratory: flexibility in framing, iterative refinement visible

Usage (standalone):
    python -m evals.artifact.analysis_depth samples/case_001/
"""

from __future__ import annotations

from typing import TYPE_CHECKING

from evals.shared.llm_judge import LLMJudge
from evals.shared.loader import CaseData, load_case
from evals.shared.results import EvalResult, Finding

if TYPE_CHECKING:
    pass

# ---------------------------------------------------------------------------
# Eval metadata (consumed by the runner registry)
# ---------------------------------------------------------------------------

EVAL_NAME = "ae-3"
REQUIRED_ARTIFACTS = ["query", "report"]

# ---------------------------------------------------------------------------
# Public eval function
# ---------------------------------------------------------------------------

def evaluate_analysis_depth(case: CaseData) -> EvalResult:
    """Run the AE-3 analysis depth eval on a loaded case.

    Returns an ``EvalResult`` with:
      - score: normalized depth score (0.0-1.0) from 1-5 scale
      - passed: True if normalized score >= 0.6
      - findings: based on criteria met/missed
    """
    judge = LLMJudge()

    prompt = f"""You are evaluating the analysis depth of a research report generated by an AI agent.

**User Query:**
{case.query}

**Generated Report:**
{case.report}

Your task:
1. **Classify the query purpose** into one of these categories:
   - "survey": Broad overview across multiple topics, approaches, or studies
   - "comparison": Direct comparison between specific approaches, methods, or interventions
   - "deep_dive": In-depth analysis of a single topic, method, or study
   - "exploratory": Open-ended exploration without a predefined structure

2. **Apply purpose-specific depth criteria**:

   For **survey** queries, assess:
   - Breadth: Does the report cover the expected range of topics?
   - Paper coverage: Are multiple relevant papers synthesized?
   - Conciseness: Is the report appropriately focused (not too superficial or too verbose)?

   For **comparison** queries, assess:
   - Cross-paper analysis: Are studies compared directly on key dimensions?
   - Dimensions: Are relevant comparison dimensions explicitly addressed?
   - Synthesis: Does the report synthesize findings to reach comparative conclusions?

   For **deep_dive** queries, assess:
   - Depth per source: Is each paper analyzed in detail?
   - Methodology: Are research methods and designs described thoroughly?
   - Limitations: Are study limitations and caveats discussed?

   For **exploratory** queries, assess:
   - Flexibility: Does the report frame the topic appropriately?
   - Connections: Are relevant connections and patterns identified?

3. **Score depth** on a 1-5 integer scale:
   - 5: Exceptional depth, fully appropriate for purpose
   - 4: Strong depth, meets purpose well with minor gaps
   - 3: Adequate depth, acceptable but with notable gaps
   - 2: Insufficient depth, misses key aspects of purpose
   - 1: Severely lacking depth or misaligned with purpose

Provide:
- purpose: Classified purpose (string)
- depth_score: Integer from 1 to 5
- purpose_criteria_met: List of criteria that were satisfied (list of strings)
- purpose_criteria_missed: List of criteria that were not satisfied (list of strings)
- reasoning: Brief explanation of your depth assessment"""

    response_schema = {
        "purpose": "string (one of: survey, comparison, deep_dive, exploratory)",
        "depth_score": "integer from 1 to 5",
        "purpose_criteria_met": "list of strings describing criteria that were satisfied",
        "purpose_criteria_missed": "list of strings describing criteria that were not satisfied",
        "reasoning": "brief explanation of your assessment",
    }

    response = judge.judge(prompt, response_schema)

    # Normalize depth_score from 1-5 to 0-1 range
    depth_score = response.get("depth_score", 1)
    if not isinstance(depth_score, (int, float)) or depth_score < 1 or depth_score > 5:
        # Handle invalid scores
        depth_score = 1

    normalized_score = (depth_score - 1) / 4.0
    passed = normalized_score >= 0.6

    # Build findings
    findings: list[Finding] = []
    purpose = response.get("purpose", "unknown")
    criteria_met = response.get("purpose_criteria_met", [])
    criteria_missed = response.get("purpose_criteria_missed", [])

    # Findings for criteria met
    for criterion in criteria_met:
        findings.append(Finding(
            severity="info",
            message=f"Criterion satisfied: {criterion}",
            location=purpose,
        ))

    # Findings for criteria missed
    for criterion in criteria_missed:
        severity = "error" if normalized_score < 0.6 else "warning"
        findings.append(Finding(
            severity=severity,
            message=f"Criterion missed: {criterion}",
            location=purpose,
        ))

    return EvalResult(
        eval_name=EVAL_NAME,
        score=normalized_score,
        max_score=1.0,
        passed=passed,
        findings=findings,
        reasoning=(
            f"{response.get('reasoning', '')}\n\n"
            f"Classified purpose: {purpose}\n"
            f"Depth score: {depth_score}/5 (normalized: {normalized_score:.2f})\n"
            f"Criteria met: {len(criteria_met)}, missed: {len(criteria_missed)}"
        ),
    )


# ---------------------------------------------------------------------------
# Standalone entry point
# ---------------------------------------------------------------------------

if __name__ == "__main__":
    import sys

    if len(sys.argv) < 2:
        print("Usage: python -m evals.artifact.analysis_depth <case_path>")
        sys.exit(1)

    case = load_case(sys.argv[1])
    result = evaluate_analysis_depth(case)
    print(f"AE-3 Analysis Depth")
    print(f"  Score:  {result.score:.2f}/{result.max_score}")
    print(f"  Passed: {result.passed}")
    print(f"  Reason: {result.reasoning}")
    if result.findings:
        print(f"  Findings:")
        for f in result.findings:
            print(f"    [{f.severity}] {f.message}")
